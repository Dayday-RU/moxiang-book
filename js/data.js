// ===== 工具函数 =====
function getNovel(id) {
    return novelData.novels.find(function(n) { return n.id === id; });
}

function getCategory(id) {
    return novelData.categories.find(function(c) { return c.id === id; });
}

function getCategoryName(id) {
    var cat = getCategory(id);
    return cat ? cat.name : '未分类';
}

function getCategoryIcon(id) {
    var cat = getCategory(id);
    return cat ? cat.icon : '📖';
}

function getNovelsByCategory(catId) {
    return novelData.novels.filter(function(n) { return n.category === catId; });
}

function searchNovels(keyword) {
    keyword = keyword.toLowerCase();
    return novelData.novels.filter(function(n) {
        return n.title.toLowerCase().indexOf(keyword) > -1 ||
               n.author.toLowerCase().indexOf(keyword) > -1 ||
               n.description.toLowerCase().indexOf(keyword) > -1;
    });
}